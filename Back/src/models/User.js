const { DataTypes } = require("sequelize");

module.exports = (sequelise) => {
  sequelise.define(
    "user",
    {
      id: {
        type: DataTypes.UUID,
        primaryKey: true,
        defaultValue: DataTypes.UUIDV4,
      },
      name: {
        type: DataTypes.STRING,
        allowNull: false,
      },
      email: {
        type: DataTypes.STRING,
        allowNull: false,
      },
      phone: {
        type: DataTypes.STRING,
        allowNull: false,
      },
      password: {
        type: DataTypes.STRING,
        allowNull: false,
      },
      role: {
        type: DataTypes.ENUM("Usuario", "Admin"),
        allowNull: true,
        defaultValue: "Usuario",
      },
    },
    { timestamps: false }
  );
};
